namespace blur_impl {
    inline ID3D11PixelShader* s_pshader = nullptr;
    inline ID3D11ShaderResourceView* s_tex = nullptr;
    inline ID3D11SamplerState* s_blurSampler = nullptr;

    inline void safe_release(IUnknown*& resource) {
        if (resource) { resource->Release(); resource = nullptr; }
    }

    inline void get_back_buffer(IDXGISwapChain* swap_chain, ID3D11Device* dev, ID3D11DeviceContext* ctx) {
        ID3D11Texture2D* back_buffer = nullptr;
        if (FAILED(swap_chain->GetBuffer(0, IID_PPV_ARGS(&back_buffer)))) return;

        D3D11_TEXTURE2D_DESC desc;
        back_buffer->GetDesc(&desc);
        desc.BindFlags = D3D11_BIND_SHADER_RESOURCE;

        ID3D11Texture2D* back_buffer_tex = nullptr;
        if (SUCCEEDED(dev->CreateTexture2D(&desc, nullptr, &back_buffer_tex))) {
            ctx->CopyResource(back_buffer_tex, back_buffer);

            D3D11_SHADER_RESOURCE_VIEW_DESC srvDesc = {};
            srvDesc.Format = desc.Format;
            srvDesc.ViewDimension = D3D11_SRV_DIMENSION_TEXTURE2D;
            srvDesc.Texture2D.MipLevels = desc.MipLevels;
            srvDesc.Texture2D.MostDetailedMip = 0;

            safe_release(reinterpret_cast<IUnknown*&>(s_tex));
            dev->CreateShaderResourceView(back_buffer_tex, &srvDesc, &s_tex);
            safe_release(reinterpret_cast<IUnknown*&>(back_buffer_tex));
        }
        safe_release(reinterpret_cast<IUnknown*&>(back_buffer));
    }

    struct BlurCbData { IDXGISwapChain* sc; ID3D11Device* dev; ID3D11DeviceContext* ctx; };
    inline BlurCbData s_cbData{};

    inline void begin_cb(const ImDrawList*, const ImDrawCmd* cmd) {
        auto* d = reinterpret_cast<BlurCbData*>(cmd->UserCallbackData);
        if (!d || !d->dev || !d->ctx) return;

        if (!s_pshader)
            d->dev->CreatePixelShader(pshaderd, sizeof(pshaderd), nullptr, &s_pshader);

        if (!s_blurSampler) {
            D3D11_SAMPLER_DESC sd{};
            sd.Filter = D3D11_FILTER_MIN_MAG_MIP_LINEAR;
            sd.AddressU = D3D11_TEXTURE_ADDRESS_CLAMP;
            sd.AddressV = D3D11_TEXTURE_ADDRESS_CLAMP;
            sd.AddressW = D3D11_TEXTURE_ADDRESS_CLAMP;
            sd.ComparisonFunc = D3D11_COMPARISON_NEVER;
            sd.MaxLOD = D3D11_FLOAT32_MAX;
            d->dev->CreateSamplerState(&sd, &s_blurSampler);
        }

        d->ctx->PSSetShader(s_pshader, nullptr, 0);
        if (s_blurSampler)
            d->ctx->PSSetSamplers(0, 1, &s_blurSampler);
    }
}

inline void draw_background_blur(
    ImDrawList* draw_list,
    IDXGISwapChain* swap_chain,
    ID3D11Device* dev,
    ID3D11DeviceContext* ctx,
    ImVec2 start, ImVec2 end,
    float rounding = 0.f,
    ImDrawFlags flags = 0)
{
    if (!draw_list || !swap_chain || !dev || !ctx) return;
    blur_impl::get_back_buffer(swap_chain, dev, ctx);
    if (!blur_impl::s_tex) return;

    blur_impl::s_cbData = { swap_chain, dev, ctx };
    draw_list->AddCallback(blur_impl::begin_cb, &blur_impl::s_cbData);
    ImVec2 ds = ImGui::GetIO().DisplaySize;
    if (ds.x < 1.f) ds.x = 1.f;
    if (ds.y < 1.f) ds.y = 1.f;
    draw_list->AddImageRounded(
        (ImTextureID)blur_impl::s_tex,
        start, end,
        ImVec2(start.x / ds.x, start.y / ds.y),
        ImVec2(end.x / ds.x, end.y / ds.y),
        ImColor{1.f, 1.f, 1.f, ImGui::GetStyle().Alpha},
        rounding, flags);
    draw_list->AddCallback(ImDrawCallback_ResetRenderState, nullptr);
}
